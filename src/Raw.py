def fromCSV(file_path : str) -> List[List[int]]:
    with open(file_path) as file:
        raw_data = [line.strip().split(',') for line in file]
    matrix = zeros((len(raw_data), len(raw_data[0])))
    for y in range(len(raw_data)):
        for x in range(len(raw_data[0])):
            matrix[y][x] = predef[raw_data[y][x].lower()]
    return matrix

